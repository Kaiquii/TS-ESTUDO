/* Crie uma função que recebe duas palavras (strings) e retorna sua concatenação. De forma resumida, deve juntar as duas palavras. */

function juntarPalavras(palavra1: string, palavra2: string, palavra3: string) {
  return palavra1 + " " + palavra2 + " " + palavra3;
}

const palavra = juntarPalavras("Kaiqui", "Lucas", "de Araujo");
console.log(palavra);
